    public function run(): void
    {
        DB::transaction(function () {
            foreach ($this->data() as $regionName => $districts) {
                $region = Region::firstOrCreate(['name' => $regionName]);

                foreach ($districts as $districtName) {
                    $district = District::firstOrCreate([
                        'region_id' => $region->id,
                        'name' => $districtName,
                    ]);

                    if ($district->mahallas()->exists()) {
                        continue;
                    }

                    foreach ($this->mahallasFor() as $mahallaName) {
                        Mahalla::create([
                            'district_id' => $district->id,
                            'name' => $mahallaName,
                        ]);
                    }

                    foreach ($this->streetsFor() as $streetName) {
                        Street::create([
                            'district_id' => $district->id,
                            'name' => $streetName,
                        ]);
                    }
                }
            }
        });
    }

